# Add function

def add(n1, n2):
    return n1 + n2


# Subtract function

def subtract(n1, n2):
    return n1 - n2


# Multiply function

def multiply(n1, n2):
    return n1 * n2


# Subtract function

def divide(n1, n2):
    return n1 / n2


operations = {
    "+": add,
    "-": subtract,
    "*": multiply,
    "/": divide,
}